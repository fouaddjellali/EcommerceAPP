    }
    #[ORM\Column(type: 'string', nullable: true)]
    private ?string $resetToken = null;

    #[ORM\Column(type: 'datetime', nullable: true)]
    private ?\DateTimeInterface $tokenRequestedAt = null;

    public function getResetToken(): ?string
    {
        return $this->resetToken;
    }

    public function setResetToken(?string $resetToken): self
    {
        $this->resetToken = $resetToken;

        return $this;
    }
    public function getTokenRequestedAt(): ?\DateTimeInterface
    {
        return $this->tokenRequestedAt;
    }
    public function setTokenRequestedAt(?\DateTimeInterface $tokenRequestedAt): self
    {
        $this->tokenRequestedAt = $tokenRequestedAt;

        return $this;
    }
    public function isTokenValid(): bool
    {
        return $this->tokenRequestedAt && $this->tokenRequestedAt->modify('+1 hour') > new \DateTime();
    }
    #[Assert\Length(min: 8, minMessage: 'Le mot de passe doit contenir au moins 8 caractÃ¨res.')]
    private ?string $plainPassword = null;

    public function setPlainPassword(?string $plainPassword): self
    {
        $this->plainPassword = $plainPassword;
        return $this;
    }

    public function getPlainPassword(): ?string
    {
        return $this->plainPassword;
    }

}
